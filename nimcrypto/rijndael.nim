    VSWAP(ctx.RKd[i + 1], ctx.RKd[j + 1])
    VSWAP(ctx.RKd[i + 2], ctx.RKd[j + 2])
    VSWAP(ctx.RKd[i + 3], ctx.RKd[j + 3])
